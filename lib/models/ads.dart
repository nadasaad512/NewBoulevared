class Autogenerated {
  bool? status;
  int? code;
  String? message;
  Advertiser? advertiser;
  List<AdvertiserADs>? advertiserADs;

  Autogenerated(
      {this.status,
      this.code,
      this.message,
      this.advertiser,
      this.advertiserADs});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    status = json['status'];
    code = json['code'];
    message = json['message'];
    advertiser = json['advertiser'] != null
        ? Advertiser.fromJson(json['advertiser'])
        : null;
    if (json['advertiserADs'] != null) {
      advertiserADs = <AdvertiserADs>[];
      json['advertiserADs'].forEach((v) {
        advertiserADs!.add(AdvertiserADs.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['status'] = status;
    data['code'] = code;
    data['message'] = message;
    if (advertiser != null) {
      data['advertiser'] = advertiser!.toJson();
    }
    if (advertiserADs != null) {
      data['advertiserADs'] = advertiserADs!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

